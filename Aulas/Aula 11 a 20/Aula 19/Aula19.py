n1 = 15
n2 = 8

def somar():
    r = n1+n2
    print("Soma de "+str(n1)+" + "+str(n2)+" = "+str(r))
def subtrair():
    r = n1-n2
    print("Subtracao de "+str(n1)+" - "+str(n2)+" = "+str(r))
def multiplicar():
    r = n1*n2
    print("Multiplicacao de "+str(n1)+" * "+str(n2)+" = "+str(r))

def calculos():
    somar()
    subtrair()
    multiplicar()

calculos()